# Lane polygon vertical extent: lanes will be built between y_top..y_bottom
# Set y_top near horizon (smaller ratio = closer to top)
HORIZON_Y_RATIO = 0.55  # 0..1
# Hough + merging params
CANNY_LOW = 80
CANNY_HIGH = 200
HOUGH_THRESH = 80
HOUGH_MIN_LINE_LEN = 80
HOUGH_MAX_LINE_GAP = 30
# Consider only "mostly vertical" lane-like lines
MIN_ABS_SLOPE = 0.4
# Merge lines whose x at bottom is within this many pixels
MERGE_X_TOL = 40

def load_image(image_path: str):
    if not os.path.exists(image_path):
        raise FileNotFoundError(f"Image not found: {image_path}")
    img = cv2.imread(image_path)
    if img is None:
        raise ValueError(f"Failed to read image: {image_path}")
    return img

def detect_lane_boundaries(image: np.ndarray,
                           canny_low: int,
                           canny_high: int,
                           hough_thresh: int,
                           min_line_len: int,
                           max_line_gap: int,
                           min_abs_slope: float,
                           merge_x_tol: int):
    h, w = image.shape[:2]
    # Edge detection
    gray = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)
    blur = cv2.GaussianBlur(gray, (5, 5), 1.2)
    edges = cv2.Canny(blur, canny_low, canny_high)

    # Hough line segments
    lines = cv2.HoughLinesP(
        edges,
        rho=1,
        theta=np.pi / 180.0,
        threshold=hough_thresh,
        minLineLength=min_line_len,
        maxLineGap=max_line_gap
    )

    if lines is None:
        return []

    # Collect candidate lane boundaries as (m, b) in y = m x + b form
    boundaries = []
    for l in lines[:, 0, :]:
        x1, y1, x2, y2 = map(float, l)
        # Avoid division by zero; keep "vertical-ish" lines by slope in x->y space
        dx = (x2 - x1)
        dy = (y2 - y1)
        if abs(dx) < 1e-3:
            # Treat as a very steep line; approximate slope by large value
            m = 1e6 if dy >= 0 else -1e6
        else:
            m = dy / dx
        if abs(m) < min_abs_slope:
            continue
        # Convert to y = m x + b
        b = y1 - m * x1
        boundaries.append((m, b))

    if not boundaries:
        return []

    # Project each boundary to bottom y = h-1; use x_bottom to cluster/merge
    y_bottom = h - 1

    def x_at_y(m, b, y):
        # y = m x + b => x = (y - b)/m
        # Handle extremely steep lines by clamping
        if abs(m) < 1e-6:
            return np.inf
        return (y - b) / m

    # Sort by x at bottom
    boundaries = sorted(boundaries, key=lambda mb: x_at_y(mb[0], mb[1], y_bottom))

    # Merge boundaries with similar x_bottom
    merged = []
    current_group = []
    prev_x = None
    for m, b in boundaries:
        xb = x_at_y(m, b, y_bottom)
        if prev_x is None or abs(xb - prev_x) <= merge_x_tol:
            current_group.append((m, b))
        else:
            merged.append(current_group)
            current_group = [(m, b)]
        prev_x = xb
    if current_group:
        merged.append(current_group)

    # Average each group to a single boundary
    merged_boundaries = []
    for group in merged:
        ms = np.array([g[0] for g in group], dtype=np.float64)
        bs = np.array([g[1] for g in group], dtype=np.float64)
        m_avg = float(np.median(ms))
        b_avg = float(np.median(bs))
        merged_boundaries.append((m_avg, b_avg))

    # Sort final boundaries by x_bottom ascending
    merged_boundaries.sort(key=lambda mb: x_at_y(mb[0], mb[1], y_bottom))
    return merged_boundaries

def build_lane_polygons_from_boundaries(boundaries, img_width: int, img_height: int, horizon_ratio: float):
    """
    Build quadrilateral polygons between adjacent boundaries.
    Each boundary is a line y = m x + b. We take y_top..y_bottom and compute x on each boundary.
    """
    y_top = int(max(0, min(img_height - 1, img_height * horizon_ratio)))
    y_bottom = img_height - 1

    def x_at_y(m, b, y):
        if abs(m) < 1e-6:
            return np.inf
        return (y - b) / m

    # If < 2 boundaries, treat whole image width as one lane
    if len(boundaries) < 2:
        lane_poly = np.array([
            [0, y_bottom],
            [0, y_top],
            [img_width - 1, y_top],
            [img_width - 1, y_bottom]
        ], dtype=np.int32)
        return [lane_poly]

    # Build vertical boundary strips
    xs_bottom = [x_at_y(m, b, y_bottom) for (m, b) in boundaries]
    xs_top = [x_at_y(m, b, y_top) for (m, b) in boundaries]

    # Clip to image range to avoid infs
    def clip_x(x):
        if not np.isfinite(x):
            return 0
        return int(np.clip(round(x), 0, img_width - 1))

    xs_bottom = list(map(clip_x, xs_bottom))
    xs_top = list(map(clip_x, xs_top))

    lane_polys = []
    for i in range(len(boundaries) - 1):
        xlb = xs_bottom[i]
        xub = xs_bottom[i + 1]
        xlt = xs_top[i]
        xut = xs_top[i + 1]

        # Ensure left <= right
        left_b = min(xlb, xub)
        right_b = max(xlb, xub)
        left_t = min(xlt, xut)
        right_t = max(xlt, xut)

        # Discard degenerate lanes
        if right_b - left_b < 5 and right_t - left_t < 5:
            continue

        poly = np.array([
            [left_b,  y_bottom],
            [left_t,  y_top],
            [right_t, y_top],
            [right_b, y_bottom]
        ], dtype=np.int32)
        lane_polys.append(poly)

    # If merging produced no valid polygons, fallback to single lane
    if not lane_polys:
        lane_poly = np.array([
            [0, y_bottom],
            [0, y_top],
            [img_width - 1, y_top],
            [img_width - 1, y_bottom]
        ], dtype=np.int32)
        lane_polys = [lane_poly]

    return lane_polys

def count_vehicles_per_lane(image_bgr: np.ndarray,
                            model: YOLO,
                            vehicle_class_ids: list[int],
                            conf_threshold: float,
                            lane_polygons: list[np.ndarray]):
    # Inference
    results = model.predict(
        source=image_bgr,
        conf=conf_threshold,
        device=0 if torch.cuda.is_available() else 'cpu',
        verbose=False,
        classes=vehicle_class_ids
    )

    h, w = image_bgr.shape[:2]
    # Initialize counts
    lane_counts = [0 for _ in lane_polygons]

    if results and len(results) > 0:
        det = results[0]
        if det.boxes is not None and len(det.boxes) > 0:
            boxes = det.boxes.xyxy.cpu().numpy()
            for (x1, y1, x2, y2) in boxes:
                cx = 0.5 * (x1 + x2)
                cy = 0.5 * (y1 + y2)
                # Check which lane polygon contains the center
                for idx, poly in enumerate(lane_polygons):
                    # pointPolygonTest expects (N,1,2)
                    res = cv2.pointPolygonTest(poly.reshape((-1, 1, 2)), (float(cx), float(cy)), False)
                    if res >= 0:
                        lane_counts[idx] += 1
                        break

    return lane_counts

def main():
    print("Loading image...")
    img = load_image(IMAGE_PATH)
    h, w = img.shape[:2]
    print(f"  Image size: {w}x{h}")

    print("Detecting lane boundaries...")
    boundaries = detect_lane_boundaries(
        img,
        CANNY_LOW,
        CANNY_HIGH,
        HOUGH_THRESH,
        HOUGH_MIN_LINE_LEN,
        HOUGH_MAX_LINE_GAP,
        MIN_ABS_SLOPE,
        MERGE_X_TOL
    )

    lane_polys = build_lane_polygons_from_boundaries(
        boundaries,
        img_width=w,
        img_height=h,
        horizon_ratio=HORIZON_Y_RATIO
    )
    num_lanes = len(lane_polys)
    print(f"  Estimated lanes: {num_lanes}")

    print("Loading YOLO model...")
    model = YOLO(MODEL)

    # Normalize names mapping across versions
    if isinstance(model.names, dict):
        id_to_name = model.names
    else:
        id_to_name = {i: n for i, n in enumerate(model.names)}
    vehicle_class_ids = [i for i, name in id_to_name.items() if name in VEHICLE_CLASSES]
    print(f"  Vehicle class IDs: {vehicle_class_ids}")

    print("Counting vehicles per lane...")
    counts = count_vehicles_per_lane(
        image_bgr=img,
        model=model,
        vehicle_class_ids=vehicle_class_ids,
        conf_threshold=CONF_THRESHOLD,
        lane_polygons=lane_polys
    )

    # Report
    print("\nRESULT")
    print(f"Number of lanes: {num_lanes}")
    for i, c in enumerate(counts, start=1):
        print(f"  Lane {i}: {c} vehicles")

    # Optional visualization
    vis = img.copy()
    # draw lane polygons
    for i, poly in enumerate(lane_polys, start=1):
        cv2.polylines(vis, [poly], isClosed=True, color=(0, 255, 0), thickness=2)
        # label
        M = cv2.moments(poly)
        if abs(M["m00"]) > 1e-6:
            cx = int(M["m10"] / M["m00"])
            cy = int(M["m01"] / M["m00"])
            cv2.putText(vis, f"Lane {i}", (cx - 30, cy), cv2.FONT_HERSHEY_SIMPLEX, 0.7, (0, 255, 0), 2, cv2.LINE_AA)

    out_path = str(Path(IMAGE_PATH).with_name("lanes_vehicles_vis.jpg"))
    cv2.imwrite(out_path, vis)
    print(f"\nSaved visualization: {out_path}")

if __name__ == "__main__":
    main()

