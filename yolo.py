    point = (float(cx), float(cy))
    # Return the first lane that contains the point
    for lane_id, poly in lane_polygons.items():
        # pointPolygonTest expects shape (N,1,2)
        poly_reshaped = poly.reshape((-1, 1, 2))
        res = cv2.pointPolygonTest(poly_reshaped, point, False)
        if res >= 0:  # inside or on edge
            return lane_id
    return None

